# The volume of a cylinder is given by
# V = pi*r^2*h
# If the diameter of the cylinder is 14 and the height is 75,
# Find the volume of the cylinder, take pi = 22/7

print("Print the result as follows: \nVolume: value")
diam = 14
pi = 22/7
height = 75
volume = pi * (diam/2)**2 * height
print(f"Volume: {volume}")

